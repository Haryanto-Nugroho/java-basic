package com.nhaary.program;

/**
 *
 * @author Haryanto
 */
public class LoopingDoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int x = 300;
        
        do {
            System.out.println("Nilai dari X : " + x);
            x++;
        } while (x < 350);
    }
    
}
